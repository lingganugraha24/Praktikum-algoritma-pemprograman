#tipe data Boolean
print(True)

#tipe data string
print("Ayo belajar Python")
print("Belajar Python Sangat Mudah")

#tipe data integer
print(20)

#tipe data Float
print(3,14)

#tipe data Hexadecimal
#print(9a)

#tipe data Complex
print(5j)

#tipe data List
print([1,2,3,4,5])
print(["satu","dua","tiga",])

#tipe data tuple
print((1,2,3,4,5))
print(("satu","dua","tiga"))

#tipe data dictionary
print({"nama":"Budi",'umur':20})

#tipe data Dictionary dimasukan kedalam variabel biodata
biodata = {"nama":"Andi",'umur':21} #proses inisialisasi variabel biodata
print(biodata) #proses pencetakan variabel biodata yang berisi tipe data Dictionary
print(type(biodata)) #fungsi untuk mengecek jenis tipe data akan tampil <clas'dict'> yang berarti dict adalah tipe data dictionary


